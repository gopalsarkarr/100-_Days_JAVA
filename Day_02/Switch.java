class Switch {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1 : System.out.println("School");
                break;
            case 2 : System.out.println("College");
                break;
            case 3: System.out.println("Movie");
                break;
            default: System.out.println("Sleep");
        }
    }
}